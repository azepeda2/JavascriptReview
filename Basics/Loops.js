// simple example of a for loop

for (var i = 0; i < 10; i++) {
    console.log(i);
}

// example of a new years countdown

for (var i = 10; i > 0; i--) {
    console.log(i);
}

console.log("Happy New Year!");

// example of a while loop

var counter = 0;
while (counter < 10) {
    console.log(counter);
    counter++;
}

